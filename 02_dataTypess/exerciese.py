# Write a Python program that performs the following operations and prints the results

# Addition

# Subtraction

# Multiplication

# Division (float result)

# Floor Division (integer result)

# Modulus (remainder)



a = 10
b = 5

# Addition
addition_result = a + b
print(f"Addition: {a} + {b} = {addition_result}")  #=> Addition: 10 + 5 = 15
# Subtraction
subtraction_result = a - b
print(f"Subtraction: {a} - {b} = {subtraction_result}")  #=> Subtraction: 10 - 5 = 5
# Multiplication
multiplication_result = a * b
print(f"Multiplication: {a} * {b} = {multiplication_result}")  #=> Multiplication: 10 * 5 = 50
# Division (float result)
division_result = a / b
print(f"Division: {a} / {b} = {division_result}")  #=> Division: 10 / 5 = 2.0
# Floor Division (integer result)
floor_division_result = a // b
print(f"Floor Division: {a} // {b} = {floor_division_result}")  #=> Floor Division: 10 // 5 = 2
# Modulus (remainder)
modulus_result = a % b
print(f"Modulus: {a} % {b} = {modulus_result}")  #=> Modulus: 10 % 5 = 0
